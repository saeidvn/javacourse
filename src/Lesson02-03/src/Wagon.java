public class Wagon {

    private int seats;
    private int number;

    public Wagon(int seats, int number) {
        this.seats = seats;
        this.number = number;
    }

    public int getSeats() {
        return seats;
    }

    public void setSeats(int seats) {
        this.seats = seats;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
